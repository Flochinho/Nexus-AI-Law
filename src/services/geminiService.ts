export async function askLegalQuestion(question: string, history: { role: 'user' | 'model', parts: { text: string }[] }[] = []): Promise<string> {
  try {
    // Map 'model' role to 'assistant' if needed, but @google/genai uses 'user' and 'model'
    // Our local history uses 'assistant' for UI, but model expects 'model'
    const contents = [
      ...history,
      { role: 'user', parts: [{ text: question }] }
    ];

    const response = await ai.models.generateContent({
      model: "gemini-3-flash-preview",
      contents: contents,
      config: {
        systemInstruction: SYSTEM_INSTRUCTION,
        temperature: 0.1,
      },
    });

    return response.text || "ბოდიში, პასუხის დამუშავება ვერ მოხერხდა.";
  } catch (error) {
    console.error("Gemini API Error:", error);
    throw error;
  }
}
